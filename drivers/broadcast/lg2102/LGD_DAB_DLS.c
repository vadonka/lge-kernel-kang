LGD_UINT8 UINCODE2WANSUNG(LGD_INT8* pData, LGD_UINT16 UCODE)
{
	LGD_UINT16 nLoop, unPos = MAX_HANGUL_BUFF;
	for(nLoop = 0 ; nLoop < MAX_HANGUL_BUFF; nLoop++)
	{
		if(UCODE == g_unJohap[nLoop]){
			unPos = nLoop;
			break;
		}
	}
	
	if(unPos >= MAX_HANGUL_BUFF) return LGD_ERROR;
	
	*pData++ = g_unWansung[unPos]>>8;
	*pData   = g_unWansung[unPos]&0xff;
	return XPAD_SUCCESS;
}

void LGD_PAD_UNICODE_CHECK(void) 
{
	LGD_INT8	cStrlen, cLoop;
	LGD_UINT8	ucSPFlag = 0,  ucDataPos = 0;
	LGD_UINT16	wBuf[XPAD_SIZE/2];

	cStrlen = strlen(g_stDls.cTransData);
	if(cStrlen == g_stDls.ucPadCnt) return;

	memset(wBuf, LGD_NULL, sizeof(wBuf));
	memcpy(wBuf, (LGD_UINT16*)g_stDls.cTransData, g_stDls.ucPadCnt);

	for(cLoop = 0; cLoop <= (g_stDls.ucPadCnt/2); cLoop++){
		wBuf[cLoop] = WORD_SWAP(wBuf[cLoop]);

		if(wBuf[cLoop] != 0x0020){
			if(wBuf[cLoop] < 0xff)
			{
				g_stDls.cTransData[ucDataPos++] = wBuf[cLoop] & 0xff;
				ucSPFlag = 0;
			}
			if(UINCODE2WANSUNG(&g_stDls.cTransData[ucDataPos], wBuf[cLoop]) == XPAD_SUCCESS){
				ucDataPos += 2;
				ucSPFlag = 0;
			}
		}
		else {
			if(!ucSPFlag && ucDataPos) {
				g_stDls.cTransData[ucDataPos++] = 0x20;
				ucSPFlag = 1;
			}
		}
	}
//  	WideCharToMultiByte(CP_ACP, 0, wBuf, g_stDls.ucPadCnt/2+1, g_stDls.cTransData, g_stDls.ucPadCnt+1, LGD_NULL, LGD_NULL);
}

LGD_UINT16 LGD_PAD_CRC_CHECK(LGD_UINT8 *pBuf, LGD_UINT16 wPos, LGD_UINT8 ucSize) 
{
    LGD_UINT8	ucLoop;
    LGD_UINT16	nCrc = 0xFFFF;
 
    for(ucLoop = 0; ucLoop < ucSize; ucLoop++){
        nCrc  = 0xFFFF & (((nCrc<<8) | (nCrc>>8)) ^ (0xFF & pBuf[wPos-ucLoop]));                        
        nCrc  = nCrc ^ ((0xFF & nCrc) >> 4);
        nCrc  = 0xFFFF & (nCrc ^ ( (((((0xFF & nCrc))<<8)|(((0xFF & nCrc))>>8))) << 4) ^ ((0xFF & nCrc) << 5));
    }
 
    return( (LGD_UINT16) 0xFFFF & (nCrc ^ 0xFFFF));
}



LGD_UINT8 LGD_DLS_DECODER(LGD_UINT8 *pucData, LGD_UINT16 wBitrate)
{
	LGD_UINT8	ucXPadInd, ucData, ucLength = 0, ucLoop = 0;
	LGD_UINT16	wCrcData, wCrcCheck, wPos = wBitrate-1;

	ST_XPAD_INFO	stXpadInfo;
	int nLoop, nPos;

	if(pucData[wPos] & 0xC0) return LGD_ERROR;
	ucXPadInd = (pucData[wPos] & 0x30) >> 4;

	switch(ucXPadInd){
	case 0:		// no X-PAD
		break;
	case 1:		// short X-PAD
		break;
	case 2:		// variable size X-PAD
		wPos = wBitrate - XPAD_POS_TYPE;
		for(ucLoop = 0; ; ){
			ucData = pucData[wPos--] & 0x1F;
			if((ucData == XPAD_TPYE_DLS_ST) || (ucData == XPAD_TPYE_DLS_CT)) break;
			if(++ucLoop == XPAD_FRAME_CHECK) return LGD_ERROR;
		}
		for(ucLoop = 0; ; ){
			ucData = pucData[wPos--] & 0x1F;
			if(ucData == XPAD_END_MARKER) break;
			if(++ucLoop == XPAD_FRAME_CHECK) return LGD_ERROR;
		}

		// Command Flag Function
		ucData = pucData[wPos--];
		if(!(ucData & 0x10)) ucLength = (ucData & 0x0F) + 1;
		else return XPAD_INIT;

		// Character Field CRC Check
		wCrcData = (pucData[wPos-ucLength-1] << 8) | pucData[wPos-ucLength-2];
		wCrcCheck = LGD_PAD_CRC_CHECK(pucData, wPos+1, ucLength+2);
		if(wCrcCheck != wCrcData) return XPAD_INIT;

		// First and Last segment function
		ucData = ucData & 0x60;
		if(ucData == 0x20 || ucData == 0x60) g_stDls.ucPadOk = 1;
		else if(ucData == 0x40){
			g_stDls.ucPadStart = 1;
			g_stDls.ucSegment = 1;
		}
		else{
			ucData = (pucData[wPos] & 0x70) >> 4;
			if(g_stDls.ucSegment != ucData) return XPAD_INIT;
			g_stDls.ucSegment++;
		}
		if(!g_stDls.ucPadStart) return XPAD_INIT;

		// Character Field data
		for(ucLoop = 0; ucLoop < ucLength; ucLoop++){
			if(g_stDls.ucPadCnt > XPAD_SIZE) return XPAD_INIT;
			g_stDls.cTransData[g_stDls.ucPadCnt++] = pucData[--wPos];
		}

		if(g_stDls.ucPadOk){
			LGD_PAD_UNICODE_CHECK();
			
			memset(&stXpadInfo, 0, sizeof(ST_XPAD_INFO));

			if(g_stDls.ucPadCnt <= LIMITE_LINE)
				memcpy(stXpadInfo.acBuffNo1, g_stDls.cTransData, g_stDls.ucPadCnt);
			else if(g_stDls.ucPadCnt < (LIMITE_LINE*2))
			{
				for(nLoop = LIMITE_LINE-1; nLoop >= 0; nLoop--)
				{
					if(g_stDls.cTransData[nLoop] != 0x20) continue;
					memcpy(stXpadInfo.acBuffNo1, g_stDls.cTransData, nLoop);
					memcpy(stXpadInfo.acBuffNo2, &g_stDls.cTransData[nLoop],g_stDls.ucPadCnt - nLoop);
					break;
				}
			}
			else {
				for(nLoop = LIMITE_LINE-1; nLoop >= 0; nLoop--)
				{
					if(g_stDls.cTransData[nLoop] != 0x20) continue;
					nPos = nLoop;
					memcpy(stXpadInfo.acBuffNo1, g_stDls.cTransData, nLoop);
					break;
				}
				for(nLoop = (LIMITE_LINE*2)-1; nLoop >= LIMITE_LINE; nLoop--)
				{
					if(g_stDls.cTransData[nLoop] != 0x20) continue;
					memcpy(stXpadInfo.acBuffNo2, &g_stDls.cTransData[nPos],nLoop - nPos);
					memcpy(stXpadInfo.acBuffNo3, &g_stDls.cTransData[nLoop],g_stDls.ucPadCnt - nLoop);
					break;
				}
			}

			return XPAD_SUCCESS;
		}
		break;
	}

	return LGD_ERROR;
}
